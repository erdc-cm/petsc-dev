  for(c = cStart; c < cEnd; ++c) {
    PetscScalar *p;

    ierr = DMComplexPointLocalRef(*dmCell, c, part, &p);CHKERRQ(ierr);
    p[0] = rank;
  }
  ierr = VecRestoreArray(*partition, &part);CHKERRQ(ierr);
  PetscFunctionReturn(0);
